const mongoose = require("mongoose");

const AttendanceSchema = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
  locationVerified: {
    type: Boolean,
    default: false,
  },
});
module.exports = mongoose.model("Attendance", AttendanceSchema);
